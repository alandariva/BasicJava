
public class JavaApplication1 {

    public static void main(String[] args) {
        int a = 5;
        int b = 3;

        System.out.println(a + b);
        System.out.println(8 * (5 / 2));

        double exp = (23 - (a - ++b) * 2) / 1.45;
        double exp2 = exp * 3;

        System.out.println(--exp * exp2);
    }

}
